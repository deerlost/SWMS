package com.inossem.wms.model.biz;

public class ErpReturn {

	private String matDocCode;
	
	private String type;
	
	private String message;
	
	private String matDocItem;
	
	private String docyear;

	public String getMatDocCode() {
		return matDocCode;
	}

	public void setMatDocCode(String matDocCode) {
		this.matDocCode = matDocCode;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getMatDocItem() {
		return matDocItem;
	}

	public void setMatDocItem(String matDocItem) {
		this.matDocItem = matDocItem;
	}

	public String getDocyear() {
		return docyear;
	}

	public void setDocyear(String docyear) {
		this.docyear = docyear;
	}
	
	
	
}